package common.vo;

public class TheaterVO {
	private String theaterCd;
	private String cinemaCd;
	private int theaterNo;
	private String screenCd;
	private String deleteYn;
	private int rowCount;
	private int colCount;
	
	public String getTheaterCd() {
		return theaterCd;
	}
	public void setTheaterCd(String theaterCd) {
		this.theaterCd = theaterCd;
	}
	public String getCinemaCd() {
		return cinemaCd;
	}
	public void setCinemaCd(String cinemaCd) {
		this.cinemaCd = cinemaCd;
	}
	public int getTheaterNo() {
		return theaterNo;
	}
	public void setTheaterNo(int theaterNo) {
		this.theaterNo = theaterNo;
	}
	public String getScreenCd() {
		return screenCd;
	}
	public void setScreenCd(String screenCd) {
		this.screenCd = screenCd;
	}
	public String getDeleteYn() {
		return deleteYn;
	}
	public void setDeleteYn(String deleteYn) {
		this.deleteYn = deleteYn;
	}
	public int getRowCount() {
		return rowCount;
	}
	public void setRowCount(int rowCount) {
		this.rowCount = rowCount;
	}
	public int getColCount() {
		return colCount;
	}
	public void setColCount(int colCount) {
		this.colCount = colCount;
	}
}

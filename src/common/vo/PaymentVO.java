package common.vo;

public class PaymentVO {

	private String payCd;
	private String memCd;
	private String nonmemName;
	private String nonmemHp;
	private int nonmemPayPass;
	private String payCancelYn;
	
	public String getPayCd() {
		return payCd;
	}
	public void setPayCd(String payCd) {
		this.payCd = payCd;
	}
	public String getMemCd() {
		return memCd;
	}
	public void setMemCd(String memCd) {
		this.memCd = memCd;
	}
	public String getNonmemName() {
		return nonmemName;
	}
	public void setNonmemName(String nonmemName) {
		this.nonmemName = nonmemName;
	}
	public String getNonmemHp() {
		return nonmemHp;
	}
	public void setNonmemHp(String nonmemHp) {
		this.nonmemHp = nonmemHp;
	}
	public int getNonmemPayPass() {
		return nonmemPayPass;
	}
	public void setNonmemPayPass(int nonmemPayPass) {
		this.nonmemPayPass = nonmemPayPass;
	}
	public String getPayCancelYn() {
		return payCancelYn;
	}
	public void setPayCancelYn(String payCancelYn) {
		this.payCancelYn = payCancelYn;
	}
	
	
}
